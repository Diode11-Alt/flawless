    (function() {
        const canvas = document.getElementById('shooting-stars-canvas');
        if (!canvas) return;
        const ctx = canvas.getContext('2d');
        
        let width = canvas.width = window.innerWidth;
        let height = canvas.height = window.innerHeight;
        
        window.addEventListener('resize', () => {
            width = canvas.width = window.innerWidth;
            height = canvas.height = window.innerHeight;
        });

        // Ambient floating stars
        const stars = [];
        const numStars = 55;
        for (let i = 0; i < numStars; i++) {
            stars.push({
                x: Math.random() * width,
                y: Math.random() * height,
                radius: Math.random() * 1.6 + 0.5,
                alpha: Math.random() * 0.7 + 0.2,
                deltaAlpha: (Math.random() * 0.01 + 0.003) * (Math.random() > 0.5 ? 1 : -1)
            });
        }

        // Shooting stars / Shooting rays
        const shootingStars = [];
        function createShootingStar() {
            return {
                x: Math.random() * width * 1.2 - width * 0.1,
                y: Math.random() * height * 0.45,
                length: Math.random() * 90 + 70,
                speed: Math.random() * 11 + 14,
                angle: Math.PI / 4, // 45 degree diagonal
                alpha: 1,
                fadeSpeed: Math.random() * 0.015 + 0.01
            };
        }

        setInterval(() => {
            if (shootingStars.length < 4 && Math.random() < 0.75) {
                shootingStars.push(createShootingStar());
            }
        }, 1600);

        function animate() {
            ctx.clearRect(0, 0, width, height);

            // Draw ambient twinkling stars
            for (let star of stars) {
                star.alpha += star.deltaAlpha;
                if (star.alpha <= 0.15 || star.alpha >= 0.85) {
                    star.deltaAlpha = -star.deltaAlpha;
                }
                ctx.beginPath();
                ctx.arc(star.x, star.y, star.radius, 0, Math.PI * 2);
                ctx.fillStyle = `rgba(186, 230, 253, ${star.alpha})`;
                ctx.shadowBlur = 6;
                ctx.shadowColor = 'rgba(56, 189, 248, 0.7)';
                ctx.fill();
            }

            // Draw shooting stars / streaks
            for (let i = shootingStars.length - 1; i >= 0; i--) {
                const s = shootingStars[i];
                const tailX = s.x - Math.cos(s.angle) * s.length;
                const tailY = s.y - Math.sin(s.angle) * s.length;

                const grad = ctx.createLinearGradient(s.x, s.y, tailX, tailY);
                grad.addColorStop(0, `rgba(255, 255, 255, ${s.alpha})`);
                grad.addColorStop(0.25, `rgba(56, 189, 248, ${s.alpha * 0.85})`);
                grad.addColorStop(1, 'rgba(14, 165, 233, 0)');

                ctx.beginPath();
                ctx.moveTo(s.x, s.y);
                ctx.lineTo(tailX, tailY);
                ctx.strokeStyle = grad;
                ctx.lineWidth = 2.2;
                ctx.lineCap = 'round';
                ctx.stroke();

                // Bright glowing head
                ctx.beginPath();
