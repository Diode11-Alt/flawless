                ctx.shadowColor = '#ffffff';
                ctx.fill();

                s.x += Math.cos(s.angle) * s.speed;
                s.y += Math.sin(s.angle) * s.speed;
                s.alpha -= s.fadeSpeed;

                if (s.alpha <= 0 || s.x > width + 100 || s.y > height + 100) {
