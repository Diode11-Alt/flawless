    <script>

        // Header Shrink & Back to Top
        const header = document.querySelector('.site-header');
        const backToTop = document.getElementById('back-to-top');
        if (header && backToTop) {
            window.addEventListener('scroll', () => {
                if (window.scrollY > 50) {
                    header.classList.add('scrolled');
                    backToTop.classList.add('visible');
                } else {
                    header.classList.remove('scrolled');
                    backToTop.classList.remove('visible');
                }
            });
        }

        if (backToTop) {
            backToTop.addEventListener('click', () => {
                window.scrollTo({ top: 0, behavior: 'smooth' });
            });
        }

        // Intersection Observer for scroll animations
        const observerOptions = { threshold: 0.1, rootMargin: '0px 0px -50px 0px' };
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('animated');
                    
                    // Number Counter Trigger
                    const countTarget = entry.target.querySelector('.stat-number');
                    if (countTarget && !countTarget.classList.contains('counted')) {
                        countTarget.classList.add('counted');
                        const target = +countTarget.getAttribute('data-target');
                        const suffix = countTarget.getAttribute('data-suffix') || '';
                        let count = 0;
                        const duration = 2000; // 2s duration
                        const increment = target / (duration / 16); // 60fps
                        
                        const updateCount = () => {
                            count += increment;
                            if (count < target) {
                                countTarget.innerText = Math.ceil(count) + suffix;
                                requestAnimationFrame(updateCount);
                            } else {
                                countTarget.innerText = target + suffix;
                            }
                        };
                        updateCount();
                    }

                    observer.unobserve(entry.target);
                }
            });
        }, observerOptions);
        document.querySelectorAll('.animate-up, .animate-left, .animate-right, .animate-fade, .animate-scale').forEach(el => observer.observe(el));

        // CTA tracking
        document.querySelectorAll('.btn-primary').forEach(button => {
            button.addEventListener('click', function() {
                console.log('Primary CTA clicked: ', this.innerText);
            });
        });

        // Mobile Menu Toggle
        const navToggle = document.querySelector('.nav-toggle');
        const navLinks = document.querySelector('.nav-links');
        if (navToggle && navLinks) {
            navToggle.addEventListener('click', () => {
                const expanded = navToggle.getAttribute('aria-expanded') === 'true' || false;
                navToggle.setAttribute('aria-expanded', !expanded);
                navLinks.classList.toggle('active');
            });
        }

        // Mobile menu toggle (the visible hamburger on mobile)
        const mobileToggle = document.querySelector('.mobile-menu-toggle');
        if (mobileToggle && navLinks) {
            mobileToggle.addEventListener('click', () => {
                navLinks.classList.toggle('active');
                const isOpen = navLinks.classList.contains('active');
                mobileToggle.setAttribute('aria-expanded', String(isOpen));
                const icon = mobileToggle.querySelector('i');
                if (isOpen) {
                    icon.classList.remove('fa-bars');
                    icon.classList.add('fa-times');
                } else {
                    icon.classList.remove('fa-times');
                    icon.classList.add('fa-bars');
                }
            });
            
            // Auto-close on link click (except dropdown toggle)
            const navAnchors = navLinks.querySelectorAll('a:not(.has-dropdown > a)');
            navAnchors.forEach(a => {
                a.addEventListener('click', () => {
                    if (window.innerWidth <= 992) {
                        navLinks.classList.remove('active');
                        mobileToggle.setAttribute('aria-expanded', 'false');
                        const icon = mobileToggle.querySelector('i');
                        if (icon) {
                            icon.classList.remove('fa-times');
                            icon.classList.add('fa-bars');
                        }
                    }
                });
            });
            
            // Dropdown toggle on mobile
            const dropdownToggles = document.querySelectorAll('.has-dropdown > a');
            dropdownToggles.forEach(toggle => {
                toggle.addEventListener('click', (e) => {
                    if (window.innerWidth <= 992) {
                        e.preventDefault();
                        toggle.parentElement.classList.toggle('open');
                    }
                });
            });
        }
        
        // Active nav state
        const currentPath = location.pathname.replace(/\/$/, '') || '/index.php';
        const currentHash = location.hash;
        document.querySelectorAll('.nav-links a').forEach(link => {
            const url = new URL(link.href);
            const linkPath = url.pathname.replace(/\/$/, '');
            const linkHash = url.hash;
            
            // Match exact page
            if (linkPath === currentPath && !linkHash) {
                link.classList.add('active');
            }
            // Match homepage links
            if ((currentPath.endsWith('index.php') || currentPath === '/') && linkPath.endsWith('index.php') && !linkHash) {
                link.classList.add('active');
            }
        });

        // FAQ Toggle
        function toggleFaq(btn) {
            const answer = btn.nextElementSibling;
            const isOpen = answer.classList.contains('open');
            // Close all
            document.querySelectorAll('.faq-answer').forEach(a => a.classList.remove('open'));
            document.querySelectorAll('.faq-question').forEach(b => b.classList.remove('open'));
            // Open this one if it wasn't open
            if (!isOpen) {
                answer.classList.add('open');
                btn.classList.add('open');
            }
        }

        // 3D Tilt Effect for Premium Cards
        const tiltCards = document.querySelectorAll('.card-1, .service-card-image');
        tiltCards.forEach(card => {
            card.addEventListener('mousemove', e => {
                const rect = card.getBoundingClientRect();
                const x = e.clientX - rect.left;
                const y = e.clientY - rect.top;
                
                const centerX = rect.width / 2;
                const centerY = rect.height / 2;
                
                // Max tilt angle 10 degrees
                const rotateX = ((y - centerY) / centerY) * -10; 
                const rotateY = ((x - centerX) / centerX) * 10;
                
                card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale3d(1.02, 1.02, 1.02)`;
                card.style.transition = 'none';
            });
            
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'perspective(1000px) rotateX(0deg) rotateY(0deg) scale3d(1, 1, 1)';
                card.style.transition = 'transform 0.6s cubic-bezier(0.25, 1, 0.5, 1)';
            });
        });
