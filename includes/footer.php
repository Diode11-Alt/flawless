    <script>

        // Header Shrink & Back to Top
        const header = document.querySelector('.site-header');
        const backToTop = document.getElementById('back-to-top');
        if (header && backToTop) {
            window.addEventListener('scroll', () => {
                if (window.scrollY > 50) {
                    header.classList.add('scrolled');
                    backToTop.classList.add('visible');
                } else {
                    header.classList.remove('scrolled');
                    backToTop.classList.remove('visible');
                }
            });
        }

        if (backToTop) {
            backToTop.addEventListener('click', () => {
                window.scrollTo({ top: 0, behavior: 'smooth' });
            });
        }

        // Intersection Observer for scroll animations
        const observerOptions = { threshold: 0.1, rootMargin: '0px 0px -50px 0px' };
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('animated');
                    
                    // Number Counter Trigger
                    const countTargets = entry.target.querySelectorAll('.stat-number');
                    countTargets.forEach(countTarget => {
                        if (!countTarget.classList.contains('counted')) {
                            countTarget.classList.add('counted');
                            const target = +countTarget.getAttribute('data-target');
                            const suffix = countTarget.getAttribute('data-suffix') || '';
                            let count = 0;
                            const duration = 2000; // 2s duration
                            const increment = target / (duration / 16); // 60fps
                            
                            const updateCount = () => {
                                count += increment;
                                if (count < target) {
                                    countTarget.innerText = Math.ceil(count) + suffix;
                                    requestAnimationFrame(updateCount);
                                } else {
                                    countTarget.innerText = target + suffix;
                                }
                            };
                            updateCount();
                        }
                    });

                    observer.unobserve(entry.target);
                }
            });
        }, observerOptions);
        document.querySelectorAll('.animate-up, .animate-left, .animate-right, .animate-fade, .animate-scale').forEach(el => observer.observe(el));

        // CTA tracking
        document.querySelectorAll('.btn-primary').forEach(button => {
            button.addEventListener('click', function() {
                console.log('Primary CTA clicked: ', this.innerText);
            });
        });

        // Mobile Slide-over Drawer & Overlay Controller
        const mobileToggle = document.querySelector('.mobile-menu-toggle');
        const navLinks = document.querySelector('.nav-links');
        const overlay = document.querySelector('.mobile-menu-overlay');
        const closeBtn = document.querySelector('.drawer-close-btn');

        function closeMobileDrawer() {
            if (navLinks) navLinks.classList.remove('active');
            if (overlay) overlay.classList.remove('active');
            if (mobileToggle) {
                mobileToggle.setAttribute('aria-expanded', 'false');
                const icon = mobileToggle.querySelector('i');
                if (icon) { icon.classList.remove('fa-times'); icon.classList.add('fa-bars'); }
            }
            document.body.style.overflow = '';
        }

        if (mobileToggle && navLinks) {
            mobileToggle.addEventListener('click', () => {
                const isOpen = !navLinks.classList.contains('active');
                if (isOpen) {
                    navLinks.classList.add('active');
                    if (overlay) overlay.classList.add('active');
                    mobileToggle.setAttribute('aria-expanded', 'true');
                    const icon = mobileToggle.querySelector('i');
                    if (icon) { icon.classList.remove('fa-bars'); icon.classList.add('fa-times'); }
                    document.body.style.overflow = 'hidden';
                } else {
                    closeMobileDrawer();
                }
            });

            if (closeBtn) closeBtn.addEventListener('click', closeMobileDrawer);
            if (overlay) overlay.addEventListener('click', closeMobileDrawer);
            
            // Auto-close on normal link click (except dropdown toggle)
            const navAnchors = navLinks.querySelectorAll('a:not(.has-dropdown > a)');
            navAnchors.forEach(a => {
                a.addEventListener('click', () => {
                    if (window.innerWidth <= 992) {
                        closeMobileDrawer();
                    }
                });
            });
            
            // Dropdown accordion toggle on mobile
            const dropdownToggles = document.querySelectorAll('.has-dropdown > a');
            dropdownToggles.forEach(toggle => {
                toggle.addEventListener('click', (e) => {
                    if (window.innerWidth <= 992) {
                        e.preventDefault();
                        toggle.parentElement.classList.toggle('open');
                        const arrow = toggle.querySelector('.dropdown-arrow');
                        if (arrow) {
                            arrow.style.transform = toggle.parentElement.classList.contains('open') ? 'rotate(180deg)' : 'rotate(0deg)';
                            arrow.style.transition = 'transform 0.2s ease';
                        }
                    }
                });
            });
        }
        
        // Active nav state
        const currentPath = location.pathname.replace(/\/$/, '') || '/index.php';
        const currentHash = location.hash;
        document.querySelectorAll('.nav-links a').forEach(link => {
            const url = new URL(link.href);
            const linkPath = url.pathname.replace(/\/$/, '');
            const linkHash = url.hash;
            
            // Match exact page
            if (linkPath === currentPath && !linkHash) {
                link.classList.add('active');
            }
            // Match homepage links
            if ((currentPath.endsWith('index.php') || currentPath === '/') && linkPath.endsWith('index.php') && !linkHash) {
                link.classList.add('active');
            }
        });

        // FAQ Toggle
        function toggleFaq(btn) {
            const answer = btn.nextElementSibling;
            const isOpen = answer.classList.contains('open');
            // Close all
            document.querySelectorAll('.faq-answer').forEach(a => a.classList.remove('open'));
            document.querySelectorAll('.faq-question').forEach(b => b.classList.remove('open'));
            // Open this one if it wasn't open
            if (!isOpen) {
                answer.classList.add('open');
                btn.classList.add('open');
            }
        }

        // 3D Tilt Effect for Premium Cards
        const tiltCards = document.querySelectorAll('.card-1, .service-card-image');
        tiltCards.forEach(card => {
            card.addEventListener('mousemove', e => {
                const rect = card.getBoundingClientRect();
                const x = e.clientX - rect.left;
                const y = e.clientY - rect.top;
                
                const centerX = rect.width / 2;
                const centerY = rect.height / 2;
                
                // Max tilt angle 10 degrees
                const rotateX = ((y - centerY) / centerY) * -10; 
                const rotateY = ((x - centerX) / centerX) * 10;
                
                card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale3d(1.02, 1.02, 1.02)`;
                card.style.transition = 'none';
            });
            
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'perspective(1000px) rotateX(0deg) rotateY(0deg) scale3d(1, 1, 1)';
                card.style.transition = 'transform 0.6s cubic-bezier(0.25, 1, 0.5, 1)';
            });
        });

        // ==========================================================================
        // COOKIE CONSENT & UTM LEAD ATTRIBUTION (GDPR & UAE PDPL COMPLIANT)
        // ==========================================================================
        // 1. Capture UTM Campaign Parameters into 30-day Cookie for CRM Attribution
        (function() {
            const urlParams = new URLSearchParams(window.location.search);
            const utmSource = urlParams.get('utm_source') || urlParams.get('source');
            const utmMedium = urlParams.get('utm_medium');
            const utmCampaign = urlParams.get('utm_campaign');
            if (utmSource) {
                const attributionData = JSON.stringify({
                    source: utmSource,
                    medium: utmMedium || 'direct',
                    campaign: utmCampaign || 'none',
                    timestamp: new Date().toISOString()
                });
                document.cookie = `primepath_lead_source=${encodeURIComponent(attributionData)}; max-age=2592000; path=/; SameSite=Lax`;
                localStorage.setItem('primepath_attribution', attributionData);
            }
        })();

        // 2. Cookie Consent Banner Logic
        window.addEventListener('DOMContentLoaded', () => {
            if (!localStorage.getItem('primepath_cookie_consent')) {
                const cookieBanner = document.createElement('div');
                cookieBanner.innerHTML = `
                    <div style="display: flex; align-items: center; justify-content: space-between; gap: 12px; flex-wrap: wrap;">
                        <span style="font-size: 13px; color: rgba(255,255,255,0.9); display: flex; align-items: center; gap: 8px;">
                            <i class="fas fa-cookie-bite" style="color: #0A84FF; font-size: 16px;"></i> We use cookies to improve your experience.
                        </span>
                        <button id="cookie-accept-all" style="background: #0A84FF; color: white; border: none; padding: 6px 16px; border-radius: 6px; font-weight: 700; font-size: 12.5px; cursor: pointer; white-space: nowrap;">Accept</button>
                    </div>
                `;
                cookieBanner.style.cssText = `
                    position: fixed;
                    bottom: 20px;
                    left: 20px;
